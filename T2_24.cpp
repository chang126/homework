#include <iostream>

using namespace std;

int main()
{
    cout<<"Input an integer"<<endl;
    int x;
    cin>>x;
    if(x%2==0)
        cout<<"x is even"<<endl;//x是偶数
    else
        cout<<"x is odd"<<endl;//x是奇数
    return 0;
}
